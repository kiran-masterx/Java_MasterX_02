package basic;

public class Sample {

	public static void main(String[] args) {
		System.out.println(123 + 123);
		String s = "Welcome@123!"; // One variable will be created, s with value = Welcome@123!
		System.out.println(s);
		String s1 = "Welcome@123456!";
		String s2 = "a";
		System.out.println(s1);

		int a = 109988;
		int b = -10;
		int c = 0;
		System.out.println(a + " " + b + " " + a);
		System.out.println(c);

		double d = 10.34;
		double d1 = 10.00;
		double d2 = 20.23123;
		double d3 = 100;
		int d4 = 100;
		System.out.println(d2);
		System.out.println(d1);
		System.out.println(d3 + " " + d4);

		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);
		System.out.println(b2);

		char c1 = 'a';
		char c2 = 'A';
		char c3 = '1';
		char c4 = '@';
		System.out.println(c1);
		System.out.println(c3);

	}
}
